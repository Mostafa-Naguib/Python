# \b =>Back Space
print("Mostafa\b Naguib")

# \new line=> Escape new line + "\"
print("My name is Mostafa\
 I am 20 years\
 And i am living in Cairo which is the capital of Egypt")

# \\=>Escape back slash
print("I was born on 2001\\10\\10")

# \"=>Escape single quotes
print("\"you either live longer to see yourself become the villan or you die a Hero \"")

# \r=>Carriage Return
print("Italy \r Egypt")

# \x=>Character Hex Value
print("\x31\x39\x37\x33")
