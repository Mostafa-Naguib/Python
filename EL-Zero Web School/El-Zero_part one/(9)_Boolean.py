# Boolean
# not True = False
# not False = True

# True values
print(bool("Mostafa"))
print(bool(999))
print(bool(3.14))
print(bool(True))
print(bool((1, 2, 33)))

print("=" * 40)

# False values
print(bool(0))
print(bool(""))
print(bool([]))
print(bool(False))
print(bool(None))

print("=" * 40)

Age = 14
print(not Age < 18)  # not True = False
